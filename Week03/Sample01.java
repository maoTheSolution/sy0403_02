class Student{
    String name;
    int age;
    double height;

    Student(String name, int age, double height){
        this.name = name;
        this.age = age;
        this.height = height;
    }

    //setter

    //getter
    public String getName(){
        return name;
    }

    public int getAge(){
        return age;
    }

    public double getHeight(){
        return height;
    }
}


